# vector_comparison.asm

# Инициализация начальных адресов
LOAD_CONST R1, 100        # R1 хранит начальный адрес исходного вектора
LOAD_CONST R2, 247        # R2 хранит число для сравнения
LOAD_CONST R3, 200        # R3 хранит начальный адрес результирующего вектора

# Заполнение исходного вектора значениями
LOAD_CONST R10, 250       # Значение для первого элемента
WRITE_MEM R10, [R1 + 0]   # Запись первого элемента в память

LOAD_CONST R10, 247       # Значение для второго элемента
WRITE_MEM R10, [R1 + 1]   # Запись второго элемента в память

LOAD_CONST R10, 123       # Значение для третьего элемента
WRITE_MEM R10, [R1 + 2]   # Запись третьего элемента в память

LOAD_CONST R10, 247       # Значение для четвертого элемента
WRITE_MEM R10, [R1 + 3]   # Запись четвертого элемента в память

# Поэлементное сравнение и запись результатов
# Сравнение первого элемента
READ_MEM R4, [R1 + 0]
EQUAL R5, R4, R2
WRITE_MEM R5, [R3 + 0]

# Сравнение второго элемента
READ_MEM R4, [R1 + 1]
EQUAL R5, R4, R2
WRITE_MEM R5, [R3 + 1]

# Сравнение третьего элемента
READ_MEM R4, [R1 + 2]
EQUAL R5, R4, R2
WRITE_MEM R5, [R3 + 2]

# Сравнение четвертого элемента
READ_MEM R4, [R1 + 3]
EQUAL R5, R4, R2
WRITE_MEM R5, [R3 + 3]
